#include "libft.h"

void	ft_bzero(void *dest, size_t n)
{
	unsigned char	*_ptr;
	size_t			i;

	i = 0;
	_ptr = (unsigned char *)dest;
	while (i < n)
	{
		_ptr[i] = 0;
		i++;
	}
}
